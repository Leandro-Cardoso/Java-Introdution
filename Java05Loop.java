public class Java05Loop {
    public static void main(String[] args) {
        /*
        Loop:
        */
        int n = 10;
        for(int i = 1; i <= n; i++) {
            System.out.println(i);
        }
        
        int i = 1;
        while(i <= n) {
            System.out.println(i);
            i++;
        }
    }
}
